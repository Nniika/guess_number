# Программа приёма заказов в кофейне
print("ДОБРО ПОЖАЛОВАТЬ В НАШУ КОФЕЙНЮ")
print("-" * 40)

# Шаг 1: Спрашиваем имя
name = input("Как вас зовут? ")
name = name.title()

# Шаг 2: Спрашиваем напиток
drink = input("Какой напиток желаете? (латте/капучино/американо) ")

# Шаг 3: Узнаём количество сахара
sugar = input("Сколько кусочков сахара? ")
sugar = int(sugar)  # Превращаем в число

# Шаг 4: Уточняем, с собой или здесь
with_you = input("С собой? (1 - да, 0 - нет) ")
int_with_you = int(with_you)
bool_with_you = bool(int_with_you)

# Шаг 5: Выводим итоговый заказ
print("ДОБРО ПОЖАЛОВАТЬ В НАШУ КОФЕЙНЮ")
print("-" * 40)
print("\n" + "="*30)
print("         ВАШ ЗАКАЗ")
print("="*30)
print("Имя:", name)
print("Напиток:", drink)
print("Сахар:", sugar, "кус.")
print("Готовим в стаканчике:", bool_with_you)
print("="*30)
print("Спасибо за заказ! Ждите 5 минут")